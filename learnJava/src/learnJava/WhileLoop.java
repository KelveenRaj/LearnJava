package learnJava;

public class WhileLoop {

	public static void main(String[] args) {
				
		int a = 0;
		
		//For Loop Example
		for(int i=0; i<10; i++) {
			System.out.println("i is less than 10 =" + i);
		}
		
		//Will loop while condition is True
		while(a < 10) {
			System.out.println("a is less than 10 = " + a);
			a++;
		}
	}
}
